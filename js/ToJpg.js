function changeImg1() {
	var obj = document.getElementById("bbb");
	if (obj.getAttribute("src") == "img/R12.png") {
		obj.setAttribute("src", "img/R12.jpg");
	} else {
		obj.setAttribute("src", "img/R12.png");
	}

}

